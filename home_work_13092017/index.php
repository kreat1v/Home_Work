// Задание 5 - Написать функцию, которая принимает один(!) аргумент - натуральное число. При каждом вызове функция должна возвращать среднее арифметическое значение переданных в нее чисел с учетом всех предыдущих вызовов.
function average ($n)
{
    static $a;
    static $i = 1;
    $a = $a + $n;
    $b = $a / $i;
    $i++;
    return $b;
}
echo average(1) . "<br />";
echo average(5) . "<br />";
echo average(3) . "<br />";
echo average(31) . "<br />";

