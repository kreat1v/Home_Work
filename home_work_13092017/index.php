// Задание 4 - Создать функцию с именем sayHello, которая принимает один аргумент - строку $name(указать тип аргумента). Функция должна выводить сначала строку Привет, $name. А затем строку, в которой будет сказанно, сколько раз функция была вызвана в формате Всего поздоровались $n раз. Вызвать функцию несколько раз с разным значением параметра.
function sayHello (string $name)
{
    static $n;
    echo "Привет, {$name}. <br />";
    $n++;
    echo "Всего поздоровались $n раз. <br />";
}
